function make_Album(artist, title, tracks) {
    artist: String;
    title: String;
    tracks: Number;
    return "".concat(artist, " ").concat(title, "  ").concat(tracks);
}
var album1 = make_Album('Tooba ', ' The first album ', 10);
console.log(album1);
var album2 = make_Album('Saba ', ' The second album ', 20);
console.log(album2);
var album3 = make_Album('Ali ', ' The Third album ', 8);
console.log(album3);
